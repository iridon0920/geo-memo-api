     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        Memo::where('id', $id)->update([
            'text' => $request->input('text'),
            'latitude' => $request->input('latitude'),
            'longitude' => $request->input('longitude'),
